class parser {

    public function process_file($path) {
        if (!is_readable($path)) {
            return false;
        }

        $parser = new \progressive_parser();
        $parser->set_file($path);

        $processor = new parser_processor($restoreid);
        $parser->set_processor($xmlprocessor);
        // TODO $parser->set_progress($progress);.
        $parser->process();
    }
}
